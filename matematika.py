def sudetis(a: float, b: float) -> float:
    """
    Apskaičiuoja dviejų skaičių sumą.

    Argumentai:
    a (float): Pirmasis skaičius.
    b (float): Antrasis skaičius.

    Grąžina:
    float: Skaičių suma.

    Pavyzdys:
    >>> sudetis(3, 5)
    8
    """
    return a + b


def daugyba(a: float, b: float) -> float:
    """
    Apskaičiuoja dviejų skaičių sandaugą.

    Argumentai:
    a (float): Pirmasis skaičius.
    b (float): Antrasis skaičius.

    Grąžina:
    float: Skaičių sandauga.

    Pavyzdys:
    >>> daugyba(3, 5)
    15
    """
    return a * b
